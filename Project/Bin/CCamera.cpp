//==============================================================================
#include "CCamera.h"

//==============================================================================
CCamera CCamera::CameraControl;

//==============================================================================
CCamera::CCamera() {
	X = 0;
	Y = 0;

	TargetMode = TARGET_MODE_NORMAL;
	speed = CCamera::CameraControl.speed;
}

//==============================================================================
void CCamera::OnMove(float MoveX, float MoveY) {
	X += MoveX;
	Y += MoveY;
}

//==============================================================================
int CCamera::GetX() {
	return (int)X;
}

//------------------------------------------------------------------------------
int CCamera::GetY() {
	return (int)Y;
}

//==============================================================================
void CCamera::SetPos(int X, int Y) {
	this->X = (float)X;
	this->Y = (float)Y;
}
//==============================================================================
